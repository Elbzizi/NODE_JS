function sum(num1, num2) {
  return num1 + num2;
}

function moins(num1, num2) { // Correction du nom de la fonction
  return num1 - num2;
}

function produit(num1, num2) {
  return num1 * num2;
} 

function div(num1, num2) {
  return num1 / num2;
}

module.exports = { sum, moins, produit, div };
